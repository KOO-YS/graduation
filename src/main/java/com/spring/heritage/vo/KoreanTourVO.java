package com.spring.heritage.vo;

public class KoreanTourVO {
	int pk;
	String id;
	int num;
	String bookDt;
	String visitDt;
	
	public int getPk() {
		return pk;
	}
	public void setPk(int pk) {
		this.pk = pk;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getBookDt() {
		return bookDt;
	}
	public void setBookDt(String bookDt) {
		this.bookDt = bookDt;
	}
	public String getVisitDt() {
		return visitDt;
	}
	public void setVisitDt(String visitDt) {
		this.visitDt = visitDt;
	}

}
