package com.spring.heritage.vo;

import java.math.BigDecimal;

public class CoordVO {
	
	BigDecimal coordX;	// x 좌표
	BigDecimal coordY;	// y 좌표
	
	public BigDecimal getCoordX() {
		return coordX;
	}
	public void setCoordX(BigDecimal coordX) {
		this.coordX = coordX;
	}
	public BigDecimal getCoordY() {
		return coordY;
	}
	public void setCoordY(BigDecimal coordY) {
		this.coordY = coordY;
	}
	
}
